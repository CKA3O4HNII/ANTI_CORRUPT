const container = document.getElementById('poems-container');
const body = document.body;

const gradients = [
  "linear-gradient(135deg, #0f2027, #203a43, #2c5364)",
  "linear-gradient(135deg, #2b5876, #4e4376)",
  "linear-gradient(135deg, #283c86, #45a247)",
  "linear-gradient(135deg, #1a2a6c, #b21f1f, #fdbb2d)",
  "linear-gradient(135deg, #000428, #004e92)",
  "linear-gradient(135deg, #232526, #414345)",
  "linear-gradient(135deg, #0f0c29, #302b63, #24243e)"
];

function pickTwoPoems() {
  const shuffled = poems.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, 2);
}

function randomGradient() {
  return gradients[Math.floor(Math.random() * gradients.length)];
}

function displayPoems() {
  container.classList.add('fade-out');
  setTimeout(() => {
    const selected = pickTwoPoems();
    container.innerHTML = selected.map(p => `
      <div class="poem">
        <h2>${p.title}</h2>
        <pre>${p.text}</pre>
      </div>
    `).join('');
    body.style.background = randomGradient();
    container.classList.remove('fade-out');
  }, 1000);
}

displayPoems();
setInterval(displayPoems, 30000);
